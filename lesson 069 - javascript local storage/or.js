let num = Number(prompt("Enter a number"))
if (num == 25 || num == 50 || num == 75 || num == 100) {
    if (num == 25) {
        console.log("Your number is 25")
    }
    else if (num == 50) {
        console.log("Your number is 50")
    }
    else if (num == 75) {
        console.log("Your number is 75")
    }
    else if (num == 100) {
        console.log("Your number is 100")
    }
}
else {
    console.log("You didnt get the right number")
}